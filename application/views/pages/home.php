<h4> Petite description de notre école</h4>
<p>Créée en 2008 , l’École de Danse vous accueille dans un espace convivial de 240 m² climatisé, avec parking, et vous invite à découvrir un lieu qui mêle à la fois les compétences d’une équipe pédagogique de qualité et une ambiance chaleureuse. Modern’jazz, contemporain, classique, hip hop, new style, zumba et pole dance sont les disciplines enseignées dans l’Ecole d'Angélique VALENTI.</p>

 
<p>
Les cours d'éveil s'adressent aux enfants à partir de 3 ans et demi. Les niveaux s'échelonnent du niveau débutant jusqu'au confirmé, de l’enfant à l’adulte, avec aussi une classe de formation professionnelle pour les jeunes danseurs désirant se diriger dans les métiers de la danse.
</p>
 
<p>
Plusieurs stages sont organisés tout au long de l'année avec des professeurs de renom et l'École participe également aux concours. Les professeurs sont Diplômés d'Etat.

 </p>
<p>
Le secrétariat est ouvert le lundi de 17:00 à 20:00, le mardi et jeudi de 13:45 à 15:45 et de 17:00 à 20:30, le mercredi de 11:00 à 18:30,le vendredi de 17:00 à 19:00 et le samedi de 10:00 à 17:30.</p>
